public class percabangan_switch_case {
     /**
     * @param args the command line arguments
     */
    
    public static void main(String[] args) {
         int ban = 123;
       
         
        switch(ban){
            case 1 :
                System.out.println("kempes");
                break;
            case 2:
                System.out.println("keras");
                break;
            case 3 :
                System.out.println("normal");
                break;
            default:
                System.out.println("normal");
       
    }

  
    }
}
